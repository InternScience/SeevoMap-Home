// 辅助函数: 获取场景中心X坐标
export function getSceneCenter(sceneKey: string): number {
  const scene = SCALE_SCENES_X.find(s => s.key === sceneKey);
  if (!scene) return 0.5;
  return (scene.range[0] + scene.range[1]) / 2;
}

// 辅助函数: 获取层级中心Y坐标
export function getLevelCenter(levelKey: string): number {
  const level = SCALE_LEVELS_Y.find(l => l.key === levelKey);
  if (!level) return 0.5;
  return (level.range[0] + level.range[1]) / 2;
}

